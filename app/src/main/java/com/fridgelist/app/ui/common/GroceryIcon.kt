/**
 * Renders a grocery icon by name.
 *
 * When a real SVG drawable asset (ic_grocery_<name>) is present it is used.
 * Until then, an emoji inside a category-coloured rounded square is shown so
 * the app is fully usable without any custom artwork.
 */
@Composable
fun GroceryIcon(
    iconName: String,
    contentDescription: String?,
    modifier: Modifier = Modifier,
    colorFilter: ColorFilter? = null
) {
    val context = LocalContext.current
    val resId = remember(iconName) {
        context.resources.getIdentifier(
            "ic_grocery_$iconName",
            "drawable",
            context.packageName
        )
    }

    if (resId != 0) {
        val vector = ImageVector.vectorResource(id = resId)
        Image(
            imageVector = vector,
            contentDescription = contentDescription,
            modifier = modifier,
            colorFilter = colorFilter
        )
    } else {
        val (emoji, _) = remember(iconName) { emojiAndColor(iconName) }

        BoxWithConstraints(
            modifier = modifier
                .then(
                    if (colorFilter != null)
                        Modifier.drawWithContent {
                            val paint = Paint().apply { this.colorFilter = colorFilter }
                            drawContext.canvas.saveLayer(
                                bounds = Rect(offset = Offset.Zero, size = size),
                                paint = paint
                            )
                            drawContent()
                            drawContext.canvas.restore()
                        }
                    else
                        Modifier
                ),
            contentAlignment = Alignment.Center
        ) {
            val fontSize = (minOf(maxWidth, maxHeight) * 0.75f).value.sp
            Text(
                text = emoji,
                fontSize = fontSize,
                textAlign = TextAlign.Center
            )
        }
    }
}

