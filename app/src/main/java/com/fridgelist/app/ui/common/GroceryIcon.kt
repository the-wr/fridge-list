private fun emojiAndColor(iconName: String): Pair<String, Color> {
    val category = IconCatalog.all.find { it.name == iconName }?.category
    val emoji = emojiMap[iconName]
        ?: category?.let { categoryFallbackEmoji[it] }
        ?: "🛒"
    val color = category?.let { categoryColors[it] } ?: Color(0xFFE0E0E0)
    return emoji to color
}

